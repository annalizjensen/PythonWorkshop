# While Loops

# patrons age:
age = 16

# loop
while age < 21:
    print("No booz for you!")
    # another year goes by. Increment age.
    age = age + 1
    print("You are now", age, "years old")

# Notice that we are outside of the "while" block
print("The loop has exited")
print("you are now", age, "years old")
